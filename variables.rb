# Variables are like a "box" where which allows to store values an reuse them.
# By convention variables names are in snake_case.

first_name = "John"
last_name = "Smith"

puts "My name is #{first_name} #{last_name}" # => "My name is John Smith"

# You can reassing values to variables.
age = 20 # => 20
age += 1 # => 21
